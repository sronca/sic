package it.istruzione.poninchiaro.model;

public class VODistribuzioneProgetti {
	
	private String fondo;
	private String categoriaIntervento;
	private long numeroProgetti;
	private double importoAutorizzato;
	private double percentualeProgetti;
	private String regione;
	
	
	
	public String getFondo() {
		return fondo;
	}
	public void setFondo(String fondo) {
		this.fondo = fondo;
	}
	public String getCategoriaIntervento() {
		return categoriaIntervento;
	}
	public void setCategoriaIntervento(String categoriaIntervento) {
		this.categoriaIntervento = categoriaIntervento;
	}
	public long getNumeroProgetti() {
		return numeroProgetti;
	}
	public void setNumeroProgetti(long numeroProgetti) {
		this.numeroProgetti = numeroProgetti;
	}
	public double getImportoAutorizzato() {
		return importoAutorizzato;
	}
	public void setImportoAutorizzato(double importoAutorizzato) {
		this.importoAutorizzato = importoAutorizzato;
	}
	public double getPercentualeProgetti() {
		return percentualeProgetti;
	}
	public void setPercentualeProgetti(double percentualeProgetti) {
		this.percentualeProgetti = percentualeProgetti;
	}
	public String getRegione() {
		return regione;
	}
	public void setRegione(String regione) {
		this.regione = regione;
	}
	

	
	

}
